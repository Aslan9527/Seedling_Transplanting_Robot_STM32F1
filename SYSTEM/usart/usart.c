void Openmv_Receive_Data(int data)//接收Openmv传过来的数据
{
	static int state = 0;
	if(state==0&&data==0xb3)
	{
		state=1;
		openmv[0]=data;
	}
	else if(state==1&&data==0xb3)
	{
		state=2;
		openmv[1]=data;
	}
	else if(state==2)
	{
		state=3;
		openmv[2]=data;
	}
	else if(state==3)
	{
		state = 4;
		openmv[3]=data;
	}
	else if(state==4)		//检测是否接受到结束标志
	{
        if(data == 0x5B)
        {
            state = 0;
            openmv[4]=data;
            Openmv_Data();
        }
        else if(data != 0x5B)
        {
            state = 0;
            for(m=0;m<5;m++)
            {
                openmv[m]=0x00;
            }           
        }
	}    
	else
		{
			state = 0;
            for(m=0;m<5;m++)
            {
                openmv[m]=0x00;
            }
		}
		//printf("%d,%d\r\n",OpenMV_X,OpenMV_Y);
}
